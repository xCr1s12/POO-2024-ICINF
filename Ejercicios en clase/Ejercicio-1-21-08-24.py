class Persona():
    def __init__(self, nombre, edad, altura, peso):
        self.nombre = nombre
        self.edad = edad
    def imc(self, altura, peso):
        self.altura = altura
        self.peso = peso
        print(f"{self.peso / (self.altura**2)}")

persona1 = Persona("Cristobal", 19)
print(f"Hola {persona1.nombre}, tienes {persona1.edad}")


persona1.imc(1.80, 75)



